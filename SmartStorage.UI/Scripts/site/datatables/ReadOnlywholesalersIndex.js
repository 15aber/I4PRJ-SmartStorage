$(document)
    .ready(function () {

        var table = $("#wholesalers")
            .DataTable({
                ajax: {
                    url: "/api/wholesalers/",
                    dataSrc: ""
                },
                columns: [
                    {
                        data: "name"
                    },
                    {
                        data: "updated",
                        render: function (data) {
                            var date = new Date(data);
                            return date.toLocaleString();
                        }
                    },
                    {
                        data: "byUser"
                    }
                ]
            });
    });